import java.util.*;

class BitWise {
    public static void main(String[] args) {
        int n = 5;
        int pos = 3;
        int bitMask = 1 << pos;
        if ((bitMask & n) == 0) {
            System.out.println("kjhgfds");
        } else {
            System.out.println("oiuytre");
        }
    }
}